#include "libft.h"
#include <stdio.h>

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;
	char	*big;
	char	*little;

	big = (char *)haystack;
	little = (char *)needle;
	i = 0;
	if (*little == '\0')
		return (big);
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && little[j] && (i + j) < len)
		{
			j++;
		}
		if (little[j] == '\0')
			return (big + i);
		i++;
	}
	return (NULL);
}
